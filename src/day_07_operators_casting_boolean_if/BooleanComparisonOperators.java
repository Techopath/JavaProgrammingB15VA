package day_07_operators_casting_boolean_if;

public class BooleanComparisonOperators {
    public static void main(String [] args) {
        boolean b = 99 != 99, b2 = 100 >= 99;
        System.out.println(b +" " +b2);
        boolean k = 10>5;
        System.out.println("10 is greater than 5: " +k);
        System.out.println(99 >= 99);
        System.out.println(88>=77);
        System.out.println(100 >= 119);
        System.out.println(7 != 7);
        System.out.println(100 == 100);
        System.out.println(100 == 1000);



    }
}
