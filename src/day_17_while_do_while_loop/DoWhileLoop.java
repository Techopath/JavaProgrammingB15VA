package day_17_while_do_while_loop;

public class DoWhileLoop {
    public static void main(String[] args) {
        int x =0;
        do {

                System.out.print(x +" ");
                x ++;


        } while (x<=10);
        System.out.println();
        int y =0;
        do {
            System.out.print(y +" ");
            y= y+2;
        }while (y<20);

    }
}
