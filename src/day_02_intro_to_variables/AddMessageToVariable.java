package day_02_intro_to_variables;

public class AddMessageToVariable {
    public static void main(String[] args){

        int books = 3;
        System.out.println("We have " +books +" books on the table");
        String city = "Chantilly";
        // output is I live in Chantilly city, VA.
        System.out.println("I live in " +city +" city, VA.");
    }
}

