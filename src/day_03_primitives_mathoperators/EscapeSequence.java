package day_03_primitives_mathoperators;

public class EscapeSequence {
    public static void main(String [] args){
        System.out.println("1. Hello \tWorld");
        System.out.println("2. Hello \nWorld");
        System.out.println("3. Hello 'World");
        System.out.println("4. Hello \"World");
        System.out.println("5. Hello \\World");
        //
        System.out.println("I love \"Java\" programming");
        System.out.println("Lets buy \"coffee\" and \"tea\"");
        //
        System.out.println("Java");
        System.out.println("Selenium");
        System.out.println("\"Java\" \n\"Selenium\"");//by adding \n we can write in two separate lines
        System.out.println("\\|/");
        System.out.println("\\_/");
        System.out.println("/");
    }
}
