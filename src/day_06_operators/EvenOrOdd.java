package day_06_operators;

public class EvenOrOdd {
    public static void main(String [] args){
        System.out.println(4%2); // 0 - 4 is even
        System.out.println(5%2); // 1 - 5 is odd
        System.out.println(100%2); // 0 - 100 is even
        int num = 11;
        System.out.println(num%8); // 1- 55 is odd

        //
        int r = 135%16;
        System.out.println (r);
    }
}
