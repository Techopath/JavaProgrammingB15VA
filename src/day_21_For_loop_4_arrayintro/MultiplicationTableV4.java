package day_21_For_loop_4_arrayintro;

public class MultiplicationTableV4 {
    public static void main(String[] args) {
        for (int i=1; i<=6;i++){

            for (int j = 1; j <=6; j++) {

                System.out.print(j*i);

            }

            System.out.println();

        }
    }
}
